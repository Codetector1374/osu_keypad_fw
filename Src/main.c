static uint8_t usb_keyboard_report[8] = {
        0x0, // Modifier,
        0x0, // Reserved,
        0x0, 0x0, 0x0, 0x0, 0x0, 0x0 // 6x Keycodes
};
void send_report() {
  usb_keyboard_report[2] = (last_report & 0x1U) ? key_map[0] : 0x0U;
  usb_keyboard_report[3] = (last_report & 0x2U) ? key_map[1] : 0x0U;
  usb_keyboard_report[4] = (last_report & 0x4U) ? key_map[2] : 0x0U;
  USBD_HID_SendReport(&hUsbDeviceFS, usb_keyboard_report, 8);
}
